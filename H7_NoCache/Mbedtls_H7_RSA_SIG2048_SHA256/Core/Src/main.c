int Parse_DataLen(unsigned char *data) {
	
	rcv_pub_key_len = data[1] << 24 | data[2] << 16 | data[3] << 8 | data[4];
	if (rcv_pub_key_len > 0) {
		pub_key_len = rcv_pub_key_len;
	}
	sig_len = (data[5] << 24) + (data[6] << 16) + (data[7] << 8) + data[8];
	msg_len = (data[9] << 24) + (data[10] << 16) + (data[11] << 8) + data[12];
	
	if (rcv_pub_key_len >= 0 && sig_len >= 0 && msg_len >= 0) {
		return (pub_key_len + msg_len + sig_len + 1);
	} else {
		return -1;
	}
}

void Parse_Key_Msg(unsigned char *data) {
	unsigned char *p = data;
	p++;
	if (rcv_pub_key_len != 0) {
		my_free(SRAMIN, pub_key);
		pub_key = my_malloc(SRAMIN, pub_key_len + 1);
		my_memclear((char*)pub_key, pub_key_len + 1);
		my_memcpy(pub_key, p, pub_key_len);
	}
	p += rcv_pub_key_len;
	sig = p;
	p += sig_len;
	msg = p;
}

