/* USER CODE BEGIN 0 */
int Parse_DataLen(unsigned char *data) {
	
	key_len = data[1] << 24 | data[2] << 16 | data[3] << 8 | data[4];
	msg_len = data[5] << 24 | data[6] << 16 | data[7] << 8 | data[8];
	
	if (key_len >= 0 && msg_len >= 0) {
		return (key_len + msg_len + 1);
	} else {
		return -1;
	}
}

void Parse_Key_Msg(unsigned char *data) {
	unsigned char *p = data;
	p++;
	key = p;
	msg = p + key_len;
}

